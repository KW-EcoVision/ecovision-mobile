class SignupData {
  String? _name;
  String? _userId;
  String? _password;

  String? get name => _name;
  set name(String? value) => _name = value;

  String? get userId => _userId;
  set userId(String? value) => _userId = value;

  String? get password => _password;
  set password(String? value) => _password = value;
}
